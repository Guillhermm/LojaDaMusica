    private void EscForn_Button_OKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EscForn_Button_OKActionPerformed
        int index = T_Fornecedor.getSelectedRow();
        if(index >= 0){
            String codigo = T_Fornecedor.getValueAt(index, 0).toString();            
            TF_CodForn.setText(codigo);            
            dispose();
        }else
            JOptionPane.showMessageDialog(null, "Selecione um Fornecedor!");
    }//GEN-LAST:event_EscForn_Button_OKActionPerformed

